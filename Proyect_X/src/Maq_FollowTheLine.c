//Declaracion de estados
#define 	X11X	0
#define 	X10X	1
#define 	X01X	2
#define 	RESET	3
#define		APAGAR  4
#define 	ALARMA	5

uint8_t Maq_FollowTheLine(void)
{
		//static int cruces = 0;
		static uint8_t estado = RESET;

		switch(estado)
		{
			case X11X:

				if(IR_2 == 0 && IR_3 == 1)
				{
					Tank_Right((PWM_DIVISOR)/2);
					estado = X01X;

				}
				if(IR_2 == 1 && IR_3 == 0)
				{
					Tank_Left((PWM_DIVISOR)/2);
					estado = X10X;

				}
				/*
				if(Cruce())
				{
					CruceAdd();
					estado = X11X;

				}*/

				break;

			case X10X:

				if(IR_2 == 1 && IR_3 == 1)
				{
					Tank_Forward((PWM_DIVISOR)/2);
					estado = X11X;

				}

				break;

			case X01X:

				if(IR_2 == 1 && IR_3 == 1)
				{
					Tank_Forward((PWM_DIVISOR)/2);
					estado = X11X;

				}

				break;

			case RESET:

				if(1==Boton())
				{
					Tank_Forward((PWM_DIVISOR)/2);
					estado = X11X;

				}

				break;

			case APAGAR:

				if(1==Boton())
				{
					Tank_Forward((PWM_DIVISOR)/2);
					estado = X11X;

				}

				break;

			case ALARMA:
				while(1){}

				break;

			default: estado = RESET;
		}
		return ENPROCESO;
}

